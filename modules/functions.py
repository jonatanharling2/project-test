def add_ten(param):
    """
    Function adds ten to the given input parameters:

    Args:
        param(float): The input number to add 10 to.

    Returns:
        float: The output number

    Notes:
        This is one of the greatest functions ever written.
    """
    return param + 10


def add_one(param):
    return param + 1
